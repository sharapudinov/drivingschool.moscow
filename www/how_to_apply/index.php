            } else {
                $('input[name=your-email]').css("border", "1px solid #b7b7b7");
            }

            if(Errors){
                $(".error_list").removeClass("dn")
                $(".error_list span").text(ErrorsList)
            }else{
                $(".error_list").addClass("dn")
                $(".error_list span").text("")

                if(!send_request_callback)
                {
                    $.ajax({
